'''

Write the necessary code to display the area and perimeter of a rectangle that has a width of 2.4 and a height of 6.4.

'''

width = 2.4
height = 6.4

area = width * height
perimeter = (width * 2) + (height * 2)

print(f"The area of this rectangle is {area}. The perimeter of this rectangle is {perimeter}.")